# podemos cambiar de base con int
binario =int("1010", 2)   # 10 en base binaria
hexadecimal = int("A", 16)     # 10 en base hexadecimal
octal = int("77", 8)     # 63 en base octal
print(octal)

#corta float
cortar = round(3.14159, 2)   # → 3.14
print(cortar)

# mini resumen
# int(x, base=10) → convierte a entero, con soporte para distintas bases.

# float(x) → convierte a número decimal.

# str(x) → convierte a cadena de texto.

# bool(x) → convierte a booleano (True/False según si el valor está vacío o no).