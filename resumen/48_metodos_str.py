# Información:

# len(s) → longitud de la cadena.

# s.isdigit() → ¿solo dígitos?

# s.isalpha() → ¿solo letras?

# s.isalnum() → ¿letras o dígitos?

# Transformación:

# s.upper() → mayúsculas.

# s.lower() → minúsculas.

# s.capitalize() → primera letra en mayúscula.

# s.strip() → quita espacios al inicio y final.

# s.replace("a","b") → reemplaza texto.

# Búsqueda y división:

# s.find("x") → posición de "x" (o -1 si no está).

# s.index("x") → igual que find, pero lanza error si no existe.

# s.split(",") → divide por separador en lista.

# s.join(lista) → une elementos de una lista en una cadena.

x = "hola ale"
print(x.split())